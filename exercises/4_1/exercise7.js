let ang = 75;

if (ang > 100 || ang < 0) {
  console.log('Invalid');
} else if (ang >= 90) {
  console.log('A');
} else if (ang >= 80) {
  console.log('B');
} else if (ang >= 70) {
  console.log('C');
} else if (ang >= 60) {
  console.log('D');
} else if (ang >= 50) {
  console.log('E');
} else {
  console.log('F');
}